    private void kirimButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kirimButtonMouseClicked
        // TODO add your handling code here:
        String penerima = penerimaTextField.getText();
        String pesan = pesanTextArea.getText();
        String pengirim = user.getNama();
        if(penerimaTextField.getText() != null && pesanTextArea.getText() != null){
            try{
                sql = "INSERT into pesan(namapengirim, namapenerima, pesan) VALUES (" + "'" +pengirim+ "'" + "," + "'" +penerima+ "'" + "," + "'" +pesan+ "'"+")";
                stat.executeUpdate(sql);
                JOptionPane.showMessageDialog(this, "Pesan Berhasil di Kirim");
                loadData();
                penerimaTextField.setText(null);
                pesanTextArea.setText(null);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }else{
                JOptionPane.showMessageDialog(this, "Semua field harus di isi");
        }
    }//GEN-LAST:event_kirimButtonMouseClicked

